using ECQRS.SqlServer.Repositories;
using System;
using System.Configuration;
using System.Data.SqlClient;
using System.IO;
using System.Reflection;

namespace ECQRS.SqlServer.EventStore
{

    public class StartupInitializer
    {
        internal static string ConnectionString = "";
        public void Initialize()
        {
            if (ConfigurationManager.ConnectionStrings["EventStore.Data"] != null)
            {
                ConnectionString = ConfigurationManager.ConnectionStrings["EventStore.Data"].ConnectionString;
            }

            using (var db = new SqlConnection(ConnectionString))
            {
                DapperRepository.BuildDefaultTable<EventEntity>(db);
            }
            
        }

        private static string AssemblyDirectory()
        {
            var codeBase = Assembly.GetExecutingAssembly().CodeBase;
            var uri = new UriBuilder(codeBase);
            var path = Uri.UnescapeDataString(uri.Path);
            return Path.GetDirectoryName(path);
        }
    }
}
